use serde_json::Map;
use serde_json::Value;

use crate::config::migration::Migration;

#[derive(Clone, Debug)]
pub struct IgnoreNodeModulesMigration;

impl Default for IgnoreNodeModulesMigration {
    fn default() -> Self {
        Self::new()
    }
}

impl IgnoreNodeModulesMigration {
    pub fn new() -> Self {
        Self
    }
}

impl Migration for IgnoreNodeModulesMigration {
    fn property_name(&self) -> &str {
        "ignoreNodeModules"
    }

    fn is_deprecated(&self) -> bool {
        true
    }

    fn run(
        &self,
        _key: &str,
        value: &Value,
        _original_config: &Map<String, Value>,
        migrated_config: &mut Map<String, Value>,
    ) {
        let paths = if value.as_bool() == Some(true) {
            vec![Value::String("node_modules/".into())]
        } else {
            Vec::new()
        };
        if !migrated_config.contains_key("ignorePaths") {
            migrated_config.insert("ignorePaths".into(), Value::Array(paths));
        }
    }

    fn box_clone(&self) -> Box<dyn Migration> {
        Box::new(self.clone())
    }
}

#[cfg(test)]
mod tests {
    use serde_json::json;
    use serde_json::Map;

    use super::IgnoreNodeModulesMigration;
    use crate::config::migration::Migration;

    #[test]
    fn property_name() {
        let m = IgnoreNodeModulesMigration::new();
        assert_eq!(m.property_name(), "ignoreNodeModules");
    }

    #[test]
    fn migrate_true_sets_ignore_paths() {
        let m = IgnoreNodeModulesMigration::new();
        let mut migrated = Map::new();
        m.run("ignoreNodeModules", &json!(true), &Map::new(), &mut migrated);
        assert_eq!(migrated["ignorePaths"], json!(["node_modules/"]));
    }

    #[test]
    fn migrate_false_sets_empty_ignore_paths() {
        let m = IgnoreNodeModulesMigration::new();
        let mut migrated = Map::new();
        m.run("ignoreNodeModules", &json!(false), &Map::new(), &mut migrated);
        assert_eq!(migrated["ignorePaths"], json!([]));
    }

    #[test]
    fn does_not_overwrite_existing_ignore_paths() {
        let m = IgnoreNodeModulesMigration::new();
        let mut migrated = Map::new();
        migrated.insert("ignorePaths".into(), json!(["vendor/"]));
        m.run("ignoreNodeModules", &json!(true), &Map::new(), &mut migrated);
        assert_eq!(migrated["ignorePaths"], json!(["vendor/"]));
    }
}
